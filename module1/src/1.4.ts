// Basic data type

// string
let firstName: string = "maksudur";

// number
let roll: number = 123;

// boolean
let isAdmin: boolean = true;

// undefine
let x: undefined = undefined;

// null
let y: null = null;

// any type
let d;

d = 1;
d = "any type";
d = true;

// non primitive data type

// Array

let friends: string[] = ["joy", "saif"];

friends.push("radil");

let eligibleRollList: number[] = [1, 2, 3];

// tuple --> Array --> order --> type of value

let coordinates: [number, number] = [1, 5];

let ageName: [number, string, boolean] = [22, "joy", true];
